export const generateRandomName = (nameType: string = 'all'): KeywordItem => {
  const types = ['korean', 'japanese', 'western'];
  const activeType = nameType === 'all' ? types[Math.floor(Math.random() * types.length)] : nameType;

  const id = `nm-${Date.now()}-${Math.floor(Math.random() * 10000)}`;

  if (activeType === 'korean') {
    const surname = KOREAN_SURNAMES[Math.floor(Math.random() * KOREAN_SURNAMES.length)];
    const givenName = KOREAN_GIVEN_NAMES[Math.floor(Math.random() * KOREAN_GIVEN_NAMES.length)];
    return {
      id,
      text: `${surname}${givenName}`,
      translation: `한국식 이름`,
      tag: 'korean'
    };
  } else if (activeType === 'japanese') {
    const surname = JAPANESE_SURNAMES[Math.floor(Math.random() * JAPANESE_SURNAMES.length)];
    const givenName = JAPANESE_GIVEN_NAMES[Math.floor(Math.random() * JAPANESE_GIVEN_NAMES.length)];
    return {
      id,
      text: `${surname.kanji} ${givenName.kanji}`,
      translation: `${surname.hiragana} ${givenName.hiragana}`,
      tag: 'japanese'
    };
  } else {
    // western
    const givenName = WESTERN_GIVEN_NAMES[Math.floor(Math.random() * WESTERN_GIVEN_NAMES.length)];
    const surname = WESTERN_SURNAMES[Math.floor(Math.random() * WESTERN_SURNAMES.length)];
    
    // 45% chance of having a middle name
    const hasMiddle = Math.random() < 0.45;
    if (hasMiddle) {
      const middle = WESTERN_MIDDLE_NAMES[Math.floor(Math.random() * WESTERN_MIDDLE_NAMES.length)];
      return {
        id,
        text: `${givenName.eng} ${middle.eng} ${surname.eng}`,
        translation: `${givenName.kor} ${middle.kor} ${surname.kor}`,
        tag: 'western'
      };
    } else {
      return {
        id,
        text: `${givenName.eng} ${surname.eng}`,
        translation: `${givenName.kor} ${surname.kor}`,
        tag: 'western'
      };
    }
  }
};

